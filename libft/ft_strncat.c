#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *t;

	t = s1 + ft_strlen(s1);
	while (*s2 && n--)
		*t++ = *s2++;
	*t = '\0';
	return (s1);
}
